#include <bits/stdc++.h>

using namespace std;

int q;

void solution(){
    string s; cin>>s;
    
    if(s.length()==1 or s[0]=='0'){
        cout<<1<<"\n"; return;
    }

    return; 
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);/**/

    cin>>q;

    while(q--){
        solution();
    }
    

    return 0;
}